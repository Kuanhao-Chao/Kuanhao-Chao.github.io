// One-time, local-only generator. Reads the OLD Jekyll repo's collections and
// writes ../src/legacy-redirects.mjs as a *static* data file (CI does not have the
// old repo checked out, so the redirect map must be inlined and committed).
//
// Run from the new site root:  node scripts/gen-legacy-redirects.mjs
//
// Mapping: each old per-item URL forwards to the closest page on the new site.
// The original per-item content is preserved verbatim at https://archive.khchao.com,
// which the 404 page also points to for anything not listed here.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const OLD = '/Users/chaokuan-hao/Documents/Kuanhao-Chao.github.io';
const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, '..', 'src', 'legacy-redirects.mjs');

const stem = (f) => f.replace(/\.(md|html)$/i, '');
const entries = (dir) =>
  readdirSync(join(OLD, dir))
    .filter((f) => /\.(md|html)$/i.test(f))
    .map((f) => {
      const txt = readFileSync(join(OLD, dir, f), 'utf8');
      const m = txt.match(/^permalink:\s*(\S+)\s*$/m);
      return { file: f, link: m ? m[1] : null };
    });

const map = {};
const add = (key, dest) => {
  const k = key.replace(/\/+$/, ''); // normalise: no trailing slash on the key
  if (k && k.startsWith('/')) map[k] = dest;
};

// Renamed "browse" sections: land the visitor on the new section page.
for (const { link } of entries('_presentations')) if (link) add(link, '/talks/');
for (const { link } of entries('_teaching')) if (link) add(link, '/teaching/');
for (const { link } of entries('_internship')) if (link) add(link, '/cv/');

// Research: three items have an exact counterpart on the new site; the rest
// land on the research index.
const researchSpecific = {
  '/researches/genome_assembly/Han1': '/research/genome-assembly/',
  '/researches/genome_indexing/WGT': '/research/pangenome-indexing/',
  '/researches/transcriptome/splam': '/research/splice-sites/',
};
for (const { link } of entries('_researches'))
  if (link) add(link, researchSpecific[link] || '/research/');

// News + portfolio have no permalink override -> default /:collection/:filestem/.
for (const { file } of entries('_news')) add('/news/' + stem(file), '/news/');
for (const { file } of entries('_portfolio')) add('/portfolio/' + stem(file), '/research/');

// Publications use external DOI permalinks (no local pages) -> nothing to forward.

const keys = Object.keys(map).sort();
const body = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`).join('\n');
const out =
  `// AUTO-GENERATED by scripts/gen-legacy-redirects.mjs — do not edit by hand.\n` +
  `// Forwards URLs from the previous Jekyll site to the closest page on the new site.\n` +
  `// Original per-item content is preserved at https://archive.khchao.com.\n` +
  `export const legacyRedirects = {\n${body}\n};\n`;

writeFileSync(OUT, out);
console.log(`wrote ${keys.length} redirects to ${OUT}`);
